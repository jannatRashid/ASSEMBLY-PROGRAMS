;PROGRAM TO TAKE INPUT FROM USER AND SEE WHETHER IT IS ENEN OR ODD
DOSSEG
.MODEL SMALL   ;MODEL DIRECTIVE
.STACK 100H   ;STACK SEGMENT
.DATA         ;DATA DIRECTIVE
VAR1 DB 10,13,  'EVEN NUMBER$'  ;INITIALIZE VAR1 USING DEFINED BYTES
VAR2 DB 10,13,  'ODD NUMBER$'  ;INITIALIZE VAR2 USING DEFINED BYTES
.CODE
MAIN PROC
MOV AX,@DATA     ;MOVING DATA MEMORY REGISTER INTO DATA REGISTER
MOV DS,AX       ;MOVING ADDRESS TO DATA SEGMENT
MOV AH,1        ;TAKE INPUT
INT 21H
MOV BL,2    ;MOV 2 TO BL
DIV BL      ;DIVIDE INTO TO 2
CMP AH,0   ;COMPARE REMINDER TO ZERO
JE L1       ;JUMP TO L1
MOV DX,OFFSET VAR2     ;OTHERVISE PRINT VAR2
MOV AH,9    ;USED TO PRINT STRING
INT 21H
JMP exit   ;EXIT JUMP
L1:
MOV DX,OFFSET VAR1   ;PRINT VAR1
MOV AH,9
INT 21H
exit:
MOV AH,4CH        ;TO EXIT REGISTER
INT 21H
MAIN ENDP
END MAIN        ;TO END THE PROGRAM
