;PROGRAM TO MULTIPLY TWO NUMBERS AND PRINT THE PRODUCT
DOSSEG             ;DOSS SEGMENT
.MODEL SMALL  ;MODEL DIRECTIVE
.STACK 100H   ;STACK SEGMENT
.DATA       ;DATA SEGMENT
.CODE
MAIN PROC
MOV AL,5   ;MOV 5 TO AL
MOV BL,9    ;MOV 9 TO BL
MUL BL     ;MULTIPLICATION COMMAND
AAM
MOV CH,AH   ;MOV AH TO CH
MOV CL,AL  ;MOV AL TO CL
MOV DL,CH    ;MOV CH TO DL
ADD DL,48     ;ADD 48 TO DL TO CONVERT NO. IN ASCII CODE
MOV AH,2     ;TO PRINT DATA
INT 21H
MOV DL,CL    ;MOV CL TO DL
ADD DL,48
MOV AH,2
INT 21H
MOV AH,4CH  ;TO EXIT REGISTER
INT 21H
MAIN ENDP   ;TO END THE MAIN PROGRAM
END MAIN
