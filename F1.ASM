;DIVISION PROGRAM AND PRINT QUOTIENT AND REMINDER
DOSSEG         ;DOSS SEGMENT
.MODEL SMALL   ; MODEL DIRECTIVE
.STACK 100H   ;STACK SEGMENT DIRECTIVE
.DATA         ;DATA DIRECTIVE USED TO DEFINE VARIABLES
Q DB ?       ;VARIABLE DEFINED FOR QUOTIENT
R DB ?        ;VARIABLE DEFINED FOR REMINDER
.CODE        
MAIN PROC
MOV AX,@DATA   ;TO CALL VARIABLES FROM DATA  DIRECTIVE
MOV DS,AX
MOV AX,21     ;DIVIDEND GO TO AX,21
MOV BL,5      ;DIVISOR GO TOBL,5
DIV BL         ;DIVIDE COMMAND
MOV Q,AL      ;MOVE QUATIENT AL TO Q
MOV R,AH       ;MOVE REMINDER AH TO R
MOV DL,Q       ;ADD Q TO DL
ADD DL,48     ;ADD 48 TO DL,NO. CONVERTS TO ASCII CODE
MOV AH,2     ;TO PRINT DATA
INT 21H
MOV DL,R    ;MOV R TO DL
ADD DL,48
MOV AH,2
INT 21H
MOV AH,4CH  ;TO EXIT REGISTER
INT 21H
MAIN ENDP    ;TO END THE PROGRAM
END MAIN
